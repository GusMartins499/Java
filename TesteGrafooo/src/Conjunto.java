
public class Conjunto {
	private String o;
	private String d;

	public void adiciona(String ori,String dest) {
	   setO(ori);
	   setD(dest);
   }

	public String getO() {
		return o;
	}

	public void setO(String o) {
		this.o = o;
	}

	public String getD() {
		return d;
	}

	public void setD(String d) {
		this.d = d;
	}
}
